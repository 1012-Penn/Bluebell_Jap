// Package snowflake 雪花算法ID生成器
//
// 实现分布式唯一ID生成，基于Twitter的雪花算法
// 技术原理：
// 1. 64位长整型ID，包含时间戳、机器ID、序列号
// 2. 时间戳：41位，支持69年
// 3. 机器ID：10位，支持1024台机器
// 4. 序列号：12位，每毫秒支持4096个ID
//
// 技术亮点：
// - 全局唯一性，支持分布式环境
// - 趋势递增，便于数据库索引
// - 高性能，单机QPS可达百万级
// - 包含时间信息，便于数据分片
package snowflake

import (
	"time"

	sf "github.com/bwmarrin/snowflake"
)

// node 雪花算法节点实例
// 每个节点实例在分布式环境中必须有不同的machineID
var node *sf.Node

// Init 初始化雪花算法节点
//
// 功能说明：
// 1. 解析起始时间，设置算法的时间基准
// 2. 创建雪花算法节点实例
// 3. 配置机器ID，确保分布式环境下的唯一性
//
// 参数说明：
// - startTime: 起始时间字符串，格式为"2006-01-02"
// - machineID: 机器ID，范围0-1023，确保分布式环境唯一性
//
// 返回值：
// - error: 初始化过程中的错误
//
// 技术亮点：
// - 可配置的起始时间，支持系统迁移
// - 机器ID配置，支持分布式部署
// - 时间基准设置，确保ID的时间顺序性
func Init(startTime string, machineID int64) (err error) {
	// 1. 解析起始时间
	// 技术亮点：使用Go标准时间格式解析
	var st time.Time
	st, err = time.Parse("2006-01-02", startTime)
	if err != nil {
		return err
	}

	// 2. 设置时间基准（纪元时间）
	// 技术亮点：将起始时间转换为毫秒时间戳
	// 雪花算法使用毫秒时间戳，提高时间精度
	sf.Epoch = st.UnixNano() / 1000000

	// 3. 创建雪花算法节点
	// 技术亮点：每个节点使用不同的machineID确保唯一性
	node, err = sf.NewNode(machineID)
	return err
}

// GenID 生成全局唯一ID
//
// 功能说明：
// 1. 生成64位长整型唯一ID
// 2. 包含时间戳、机器ID、序列号信息
// 3. 保证全局唯一性和趋势递增性
//
// 返回值：
// - int64: 64位唯一ID
//
// 技术亮点：
// - 线程安全，支持高并发
// - 高性能，单机QPS可达百万级
// - 趋势递增，便于数据库索引优化
// - 包含时间信息，便于数据分片和排序
//
// ID结构说明：
// | 0 | 00000000000000000000000000000000000000000 | 0000000000 | 000000000000 |
// | 1位符号位 | 41位时间戳 | 10位机器ID | 12位序列号 |
func GenID() int64 {
	return node.Generate().Int64()
}
