// Package models 数据模型包
//
// 定义系统中所有的数据结构，包括：
// 1. 数据库实体模型
// 2. 请求参数模型
// 3. 响应数据模型
// 4. 业务逻辑模型
//
// 技术亮点：
// - 使用结构体标签进行数据绑定
// - 支持JSON序列化和反序列化
// - 支持数据库字段映射
// - 支持参数验证标签

// User 用户数据模型
//
// 功能说明：
// 1. 定义用户的基本信息结构
// 2. 支持数据库操作和JSON序列化
// 3. 包含用户认证相关字段
//
// 字段说明：
// - UserID: 用户唯一标识，使用雪花算法生成
// - Username: 用户名，用于登录和显示
// - Password: 密码，加密存储
// - Token: JWT令牌，不存储到数据库
//
// 技术亮点：
// - 使用db标签映射数据库字段
// - 支持JSON序列化（无json标签时使用字段名）
// - 分离敏感信息（Token不存储到数据库）
type User struct {
	UserID   int64  `db:"user_id"`  // 用户ID，对应数据库字段
	Username string `db:"username"` // 用户名，对应数据库字段
	Password string `db:"password"` // 密码，对应数据库字段（加密存储）
	Token    string // JWT令牌，不存储到数据库
}
